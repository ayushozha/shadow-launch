"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { useEffect, useState } from "react";
import type { Run } from "@/lib/types";
import { loadDemoRun } from "@/lib/loadDemoRun";

// Bottom of the results page. Four actions:
//   1. Run again           → back to the landing CTA
//   2. Copy shareable URL  → the address bar, for slack/email pasting
//   3. Copy as Markdown    → plain-text launch plan for Notion/docs/email
//   4. Download JSON       → full Run object as run_<id>.json for archiving
//
// The results page doesn't pass the Run in as a prop (we aren't allowed to
// edit it), so this component loads the same cached JSON via /lib/loadDemoRun
// for the Markdown / JSON exports. Client-side fetch is fine here — these
// actions are user-initiated after the page has hydrated.

type CopyState = "idle" | "copied";

function runIdFromPath(pathname: string | null): string {
  if (!pathname) return "demo-linear-001";
  const parts = pathname.split("/").filter(Boolean);
  const idx = parts.indexOf("results");
  if (idx >= 0 && parts[idx + 1]) return parts[idx + 1];
  return "demo-linear-001";
}

function buildMarkdown(run: Run): string {
  const lines: string[] = [];
  const winner = run.winner;
  const winningWedge = run.wedges.find((w) => w.id === winner.wedge_id);
  const headline = winningWedge?.headline ?? winner.wedge_id;
  const score = winner.final_score.toFixed(3);

  lines.push(`# Launch Plan: ${headline}`);
  lines.push("");
  lines.push(`**Winning wedge:** ${headline} (${score})  `);
  lines.push(`**Why it won:** ${winner.why_it_won}`);
  lines.push("");

  lines.push("## Tasks");
  const tasks = run.launch_board?.tasks ?? [];
  for (const t of tasks) {
    const rt = t as typeof t & { description?: string; due_day?: number };
    const day = rt.due_day ?? rt.day;
    const dayStr = day !== undefined ? `day ${day}` : "day —";
    lines.push(`- [ ] ${rt.title} — ${rt.owner} (${dayStr})`);
    const desc = rt.description ?? rt.notes;
    if (desc) lines.push(`  ${desc}`);
  }
  lines.push("");

  lines.push("## Timeline");
  const timeline = run.launch_board?.timeline ?? [];
  for (const m of timeline) {
    const rm = m as typeof m & { milestone?: string };
    lines.push(`- Day ${rm.day}: ${rm.milestone ?? rm.label}`);
  }
  lines.push("");

  lines.push("## Executive summary");
  lines.push(run.launch_board?.executive_summary ?? "");
  lines.push("");

  lines.push(
    `_Generated by Shadow Launch · run ${run.run_id} · ${run.created_at}_`,
  );
  return lines.join("\n");
}

function downloadJson(run: Run): void {
  const data = JSON.stringify(run, null, 2);
  const blob = new Blob([data], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `run_${run.run_id}.json`;
  // Append → click → revoke. Works across Chromium, Firefox, Safari.
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  // Revoke on next tick so the download handler has seen the blob URL.
  setTimeout(() => URL.revokeObjectURL(url), 0);
}

export default function ReRunExport() {
  const pathname = usePathname();
  const runId = runIdFromPath(pathname);
  const [run, setRun] = useState<Run | null>(null);
  const [copiedUrl, setCopiedUrl] = useState<CopyState>("idle");
  const [copiedMd, setCopiedMd] = useState<CopyState>("idle");
  const [downloading, setDownloading] = useState(false);

  // Load the Run once on mount for the Markdown / JSON actions. The visible
  // Launch Board above already uses the same cache, so this is a warm fetch.
  useEffect(() => {
    let cancelled = false;
    const controller = new AbortController();
    loadDemoRun(controller.signal)
      .then((r) => {
        if (!cancelled) setRun(r);
      })
      .catch(() => {
        // Silent: the two buttons will be disabled when run is null.
      });
    return () => {
      cancelled = true;
      controller.abort();
    };
  }, []);

  const copyUrl = async () => {
    if (typeof window === "undefined") return;
    try {
      await navigator.clipboard.writeText(window.location.href);
      setCopiedUrl("copied");
      setTimeout(() => setCopiedUrl("idle"), 2000);
    } catch {
      // clipboard denied — graceful noop, the URL is in the address bar already.
    }
  };

  const copyMarkdown = async () => {
    if (!run) return;
    try {
      const md = buildMarkdown(run);
      await navigator.clipboard.writeText(md);
      setCopiedMd("copied");
      setTimeout(() => setCopiedMd("idle"), 2000);
    } catch {
      // clipboard denied — no fallback here; the Download JSON path still works.
    }
  };

  const onDownloadJson = () => {
    if (!run) return;
    setDownloading(true);
    try {
      // Swap the cached run's id for the url-visible id so the file reflects
      // what the user was looking at when they hit the button.
      const stamped: Run = { ...run, run_id: runId ?? run.run_id };
      downloadJson(stamped);
    } finally {
      setTimeout(() => setDownloading(false), 600);
    }
  };

  const exportsReady = run !== null;

  return (
    <section className="px-5 py-16 md:px-10 md:py-24">
      <div className="mx-auto flex max-w-[1240px] flex-col gap-10">
        <div className="grid grid-cols-1 gap-6 md:grid-cols-[1fr_3fr] md:items-baseline">
          <span className="font-mono text-[10px] uppercase tracking-[0.24em] text-[var(--muted)]">
            §06 · Re-run / export
          </span>
          <h2
            className="font-serif text-[clamp(28px,3.5vw,44px)] leading-[1.05] tracking-[-0.02em] text-[var(--ink)]"
            style={{ fontVariationSettings: '"opsz" 60, "wght" 380, "SOFT" 40' }}
          >
            Share the run. Or <em className="italic text-[var(--accent)]">rehearse again.</em>
          </h2>
        </div>

        <div className="flex flex-col gap-4 border-t border-[var(--rule-soft)] pt-8">
          <div className="flex flex-wrap items-center gap-3">
            <Link
              href="/"
              className="inline-flex items-center gap-2 border border-[var(--ink)] bg-[var(--ink)] px-5 py-3 font-mono text-[11px] uppercase tracking-[0.18em] text-[var(--paper)] transition-colors hover:border-[var(--accent)] hover:bg-[var(--accent)]"
            >
              Run again
              <span>→</span>
            </Link>
            <button
              type="button"
              onClick={copyUrl}
              className="inline-flex items-center gap-2 border border-[var(--ink)] px-5 py-3 font-mono text-[11px] uppercase tracking-[0.18em] text-[var(--ink)] transition-colors hover:bg-[var(--ink)] hover:text-[var(--paper)]"
            >
              {copiedUrl === "copied" ? "Copied ✓" : "Copy shareable URL"}
            </button>
            <button
              type="button"
              onClick={copyMarkdown}
              disabled={!exportsReady}
              className="inline-flex items-center gap-2 border border-[var(--ink)] px-5 py-3 font-mono text-[11px] uppercase tracking-[0.18em] text-[var(--ink)] transition-colors hover:bg-[var(--ink)] hover:text-[var(--paper)] disabled:cursor-not-allowed disabled:border-[var(--rule)] disabled:text-[var(--muted)] disabled:hover:bg-transparent disabled:hover:text-[var(--muted)]"
            >
              {copiedMd === "copied" ? "Copied ✓" : "Copy as Markdown"}
            </button>
            <button
              type="button"
              onClick={onDownloadJson}
              disabled={!exportsReady}
              className="inline-flex items-center gap-2 border border-[var(--ink)] px-5 py-3 font-mono text-[11px] uppercase tracking-[0.18em] text-[var(--ink)] transition-colors hover:bg-[var(--ink)] hover:text-[var(--paper)] disabled:cursor-not-allowed disabled:border-[var(--rule)] disabled:text-[var(--muted)] disabled:hover:bg-transparent disabled:hover:text-[var(--muted)]"
            >
              {downloading ? "Downloading…" : "Download JSON"}
            </button>
          </div>
          <span className="font-mono text-[10px] uppercase tracking-[0.16em] text-[var(--muted)]">
            anyone with the link sees this run · markdown drops into notion · json archives the full payload
          </span>
        </div>
      </div>
    </section>
  );
}
